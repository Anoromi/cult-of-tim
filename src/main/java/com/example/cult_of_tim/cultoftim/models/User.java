package com.example.cult_of_tim.cultoftim.models;

public class User {

    private String email;
    private String password;

    /**
     * Getter for an email
     * @return email
     */
    public String getEmail()
    {
        return email;
    }

    /**
     * Getter for a password
     * @return password
     */
    public String getPassword()
    {
        return password;
    }
}
